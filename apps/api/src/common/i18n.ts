export function isApiLocale(value: unknown): value is ApiLocale {
  return (
    typeof value === 'string' &&
    (apiLocales as readonly string[]).includes(value)
  );
}

export function resolveApiLocale(
  header: string | string[] | undefined,
): ApiLocale {
  const raw = Array.isArray(header) ? header.join(',') : header;
  if (!raw) return DEFAULT_API_LOCALE;

  for (const candidate of raw.split(',')) {
    const tag = candidate.trim().split(';')[0]?.toLowerCase();
    if (!tag) continue;

    const base = tag.split('-')[0];
    if (isApiLocale(base)) return base;
  }

  return DEFAULT_API_LOCALE;
}

export function translateApiText(text: string, locale: ApiLocale): string {
  if (locale === 'en') return text;

  const normalized = text.replace(/\s+/g, ' ').trim();
  if (!normalized || /^[A-Z0-9_]+$/.test(normalized)) return text;

  const cooldown = normalized.match(
    /^Please wait (\d+)s before requesting another code$/,
  );
  if (cooldown?.[1]) {
    const suffix: Record<NonEnglishApiLocale, string> = {
      fr: `Veuillez patienter ${cooldown[1]} s avant de demander un autre code`,
      pt: `Aguarde ${cooldown[1]} s antes de solicitar outro código`,
      ar: `يرجى الانتظار ${cooldown[1]} ثانية قبل طلب رمز آخر`,
      sw: `Tafadhali subiri ${cooldown[1]}s kabla ya kuomba msimbo mwingine`,
      ha: `Da fatan jira ${cooldown[1]}s kafin neman wata lamba`,
      es: `Espera ${cooldown[1]} s antes de solicitar otro código`,
    };
    return suffix[locale];
  }

  return apiTranslations[normalized]?.[locale] ?? text;
}

export function translateApiValue(value: unknown, locale: ApiLocale): unknown {
  if (typeof value === 'string') return translateApiText(value, locale);
  if (Array.isArray(value))
    return value.map((item) => translateApiValue(item, locale));
  return value;
}
